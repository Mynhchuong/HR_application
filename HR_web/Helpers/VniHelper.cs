        public static string VniToUnicode(string vniString)
        {
            if (string.IsNullOrEmpty(vniString)) return vniString;

            string result = vniString;
            foreach (var (vni, unicode) in Map)
            {
                result = result.Replace(vni, unicode);
            }
            return result;
